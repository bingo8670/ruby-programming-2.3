3.4 其他文件的引用
大部分编程语言都提供了把多个程序组合为一个程序的功能。像这样，被其他程序引用的程序，我们成为库（library）。一般使用 require 方法或者 require_relative 来引用库（可省略后缀.rb）。
require 希望使用的库名
require_relative 希望使用的库名
