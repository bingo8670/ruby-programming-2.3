2.2.1  什么是符号
在标识符的开头加上“：”就是符号了，例如 sym = :foo
符号和字符串互换：对符号使用to_s方法，可得到对应的字符串；对字符串使用to_sym方法，可得到对应的符号；

2.2.2  散列的创建
{key:value}，通常使用符号/字符串/数值作为散列的键

符号当键使用是，ruby的专用简写，下面两个意思相同：
person1 = { :name => "后藤", :pinyin => "houteng" }
person2 = { name: "后藤", pinyin: "houteng" }

2.2.3 散列的使用
从散列中取出对象： 散列名 [键]
保存对象： 散列名 [键] = 希望保存的对象

2.2.4 散列的循环
